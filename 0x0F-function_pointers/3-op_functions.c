#include "3-calc.h"

/**
* op_add- Sum 2 numbers
* @a: Number 1
* @b: Number 2
* Return: Result
*/

int op_add(int a, int b)
{
return (a + b);
}

/**
* op_sub- Subtract 2 numbers
* @a: Number 1
* @b: Number 2
* Return: Result
*/

int op_sub(int a, int b)
{
return (a - b);
}

/**
* op_mul- Multiplay 2 numbers
* @a: Number 1
* @b: Number 2
* Return: Prroduct
*/

int op_mul(int a, int b)
{
return (a * b);
}

/**
* op_div- Divide 2 numbers
* @a: Number 1
* @b: Number 2
* Return: Division
*/

int op_div(int a, int b)
{
return (a / b);
}

/**
* op_mod- Modulate 2 numbers
* @a: Number 1
* @b: Number 2
* Return: Module
*/

int op_mod(int a, int b)
{
return (a % b);
}
